#!/bin/bash
# CommandCenter Operation
# Name: Summarize Rules
# Category: maintenance

# Generates a prompt for an AI agent to regenerate RulesEngine/CLAUDE_RULES.md
# from RulesEngine/BUSINESS_RULES.md and related source files.
#
# Usage:
#   bash bin/summarize_rules.sh > rules-prompt.md
#   # Feed rules-prompt.md to an AI agent — paste output over RulesEngine/CLAUDE_RULES.md
#
# Exit codes:
#   0  Prompt generated successfully
#   1  Missing required files

set -euo pipefail

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CLAUDE_RULES="$REPO_DIR/RulesEngine/CLAUDE_RULES.md"
BUSINESS_RULES="$REPO_DIR/RulesEngine/BUSINESS_RULES.md"

if [ ! -f "$CLAUDE_RULES" ]; then
    echo "ERROR: CLAUDE_RULES.md not found at $CLAUDE_RULES" >&2
    exit 1
fi

if [ ! -f "$BUSINESS_RULES" ]; then
    echo "ERROR: BUSINESS_RULES.md not found at $BUSINESS_RULES" >&2
    exit 1
fi

# --- Auto-increment version ---
CURRENT_VERSION=$(grep '^\*\*Version:\*\*' "$CLAUDE_RULES" | head -1 | sed 's/\*\*Version:\*\*[[:space:]]*//' | tr -d '\r')
VERSION_DATE=$(echo "$CURRENT_VERSION" | grep -oE '^[0-9]{8}' || echo "")
VERSION_SEQ=$(echo "$CURRENT_VERSION" | grep -oE 'V([0-9]+)$' | grep -oE '[0-9]+' || echo "0")

TODAY=$(date '+%Y%m%d')

if [ "$TODAY" = "$VERSION_DATE" ]; then
    NEXT_SEQ=$((VERSION_SEQ + 1))
    NEXT_VERSION="${TODAY} V${NEXT_SEQ}"
else
    NEXT_VERSION="${TODAY} V1"
fi

# --- emit_file helper ---
emit_file() {
    local filepath="$1"
    local label="$2"
    if [ -f "$filepath" ]; then
        echo "---"
        echo ""
        echo "## $label"
        echo ""
        cat "$filepath"
        echo ""
        echo ""
    fi
}

# --- Collect RulesEngine .md files (exclude README.md and CLAUDE_RULES.md) ---
RULES_ENGINE_DIR="$REPO_DIR/RulesEngine"

# --- Prompt header ---
cat <<HEADER
# Generate CLAUDE_RULES.md — Prompt

You are regenerating \`RulesEngine/CLAUDE_RULES.md\` from its source files.

## Task

1. Read \`BUSINESS_RULES.md\` — extract every \`**Rule text:**\` block (one per rule).
   The Rule text block ends at the next \`---\` separator or the next \`### \` heading.
2. Read the other source files for context on what rules apply.
3. Produce a new \`CLAUDE_RULES.md\` that:
   - Begins with \`# CLAUDE_RULES_START\` (exactly as shown, on its own line)
   - Second line is blank
   - Third and fourth lines are the do-not-edit notice (exactly):
     \`> Generated by \`bin/summarize_rules.sh\` — do not edit directly.\`
     \`> Edit \`RulesEngine/BUSINESS_RULES.md\` then regenerate.\`
   - Fifth line is blank
   - Then \`# DEFAULT DEVELOPMENT RULES\`
   - Then blank line
   - Then \`**Version:** ${NEXT_VERSION}\`
   - Then \`**Description:** Agent behavior contract distributed to all AI agent projects — covers git, error handling, and completion standards\`
   - Then blank line
   - Then \`Full specification and methodology: \`Specifications/AGENTS.md\` in the Specifications repository. This condensed version covers agent behavior only.\`
   - Then \`---\`
   - Then each rule section, in the same order as in BUSINESS_RULES.md, using ONLY the Rule text content
   - Ends with \`# CLAUDE_RULES_END\` (exactly as shown, on its own line)

## Rules

- Preserve \`# CLAUDE_RULES_START\` and \`# CLAUDE_RULES_END\` markers exactly — injection scripts depend on them
- Extract ONLY the \`**Rule text:**\` content — omit Scope, Applies at, Requirement, and Rationale fields
- Keep section headings (## Git Workflow, ## Project Layout, etc.) but derive them from the BUSINESS_RULES.md section names
- Do not add any content that is not in the Rule text blocks
- The output should be a complete, standalone CLAUDE_RULES.md — paste it directly over the existing file

---

HEADER

# --- Source files ---
echo "# SOURCE FILES"
echo ""

emit_file "$BUSINESS_RULES" "BUSINESS_RULES.md (RulesEngine/BUSINESS_RULES.md)"

# All other .md files in RulesEngine/ except README.md and CLAUDE_RULES.md
for f in $(find "$RULES_ENGINE_DIR" -maxdepth 1 -name '*.md' | sort); do
    fname="$(basename "$f")"
    case "$fname" in
        README.md|CLAUDE_RULES.md|BUSINESS_RULES.md) continue ;;
        *) emit_file "$f" "$fname (RulesEngine/$fname)" ;;
    esac
done

# --- Current CLAUDE_RULES.md for reference ---
echo "---"
echo ""
echo "## Current CLAUDE_RULES.md (for structure reference only)"
echo ""
cat "$CLAUDE_RULES"
echo ""
echo ""

# --- Footer ---
cat <<FOOTER
---

# END OF PROMPT

Produce the complete new \`CLAUDE_RULES.md\` content now.
Use version \`${NEXT_VERSION}\`.
Begin with \`# CLAUDE_RULES_START\` and end with \`# CLAUDE_RULES_END\`.
FOOTER
